package functionQuestions;

public class sumProd {
    
    static int sumNum(int a, int b){
        return a+b;
    }

    static int prod(int a, int b){
        return a*b;
    }



    public static void main(String[] args) {
        System.out.println("Sum = "+sumNum(20, 34));
        System.out.println("Product = "+prod(4,5));
    }
}
